class Solution:
    def rotate(self, matrix):
        # 90 deg clockwise = transpose then reverse each row
        n = len(matrix)
        for i in range(n):
            for j in range(i + 1, n):
                matrix[i][j], matrix[j][i] = matrix[j][i], matrix[i][j]
        for row in matrix:
            row.reverse()
